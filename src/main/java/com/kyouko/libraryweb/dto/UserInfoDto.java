package com.kyouko.libraryweb.dto;

import lombok.Data;

/**
 * 用户信息数据传输对象 (User Info Data Transfer Object)
 * 
 * 用于在不同层之间传递用户基本信息的数据载体。
 * 
 * 主要用途：
 * - 用户信息查询和展示
 * - 用户资料更新
 * - API接口的响应数据格式
 * - 用户管理功能的数据传递
 * 
 * 安全特性：
 * - 不包含敏感信息如密码
 * - 只包含可以对外展示的用户信息
 * - 适合在前端显示和编辑
 * 
 * 设计原则：
 * - 遵循最小权限原则，只包含必要的用户信息
 * - 与前端需求匹配的字段结构
 * - 便于JSON序列化和反序列化
 * 
 * 使用场景：
 * - 用户个人信息页面展示
 * - 管理员查看用户列表
 * - 用户资料编辑表单
 * - 借阅记录中的用户信息关联
 * 
 * @author kyouko
 * @version 1.0
 */
@Data  // Lombok注解，自动生成getter、setter、toString、equals、hashCode方法
public class UserInfoDto {
    
    /**
     * 用户唯一标识ID
     * 
     * 数据库主键，用于唯一标识一个用户。
     * 用于用户相关操作的关联和引用。
     */
    private Long id;
    
    /**
     * 用户名 (登录名)
     * 
     * 用户登录系统时使用的唯一标识符。
     * 特点：
     * - 全系统唯一，不可重复
     * - 通常不允许修改
     * - 用于登录认证
     * - 在借阅记录中作为冗余字段存储
     */
    private String username;
    
    /**
     * 用户角色
     * 
     * 标识用户在系统中的角色类型。
     * 可能的值：
     * - "ADMIN": 管理员角色，拥有系统管理权限
     * - "READER": 读者角色，可以借阅图书
     * 
     * 作用：
     * - 权限控制的基础
     * - 决定用户可以访问的功能模块
     * - 影响界面显示内容
     */
    private String role;
    
    /**
     * 用户昵称 (显示名称)
     * 
     * 用户的友好显示名称，用于界面展示。
     * 特点：
     * - 可以包含中文、英文、数字等
     * - 允许修改
     * - 用于提升用户体验
     * - 在借阅记录等地方显示
     */
    private String nickName;
    
    /**
     * 用户地址
     * 
     * 用户的联系地址信息。
     * 用途：
     * - 用户档案信息
     * - 可能用于图书邮寄等功能
     * - 用户信息完整性
     * 
     * 注意：此字段为可选信息。
     */
    private String address;
    
    /**
     * 用户电话号码
     * 
     * 用户的联系电话。
     * 用途：
     * - 联系用户的重要方式
     * - 逾期通知等功能
     * - 用户信息搜索
     * - 身份验证的辅助信息
     * 
     * 格式：通常为手机号码格式。
     */
    private String phone;
    
    /**
     * 用户邮箱地址
     * 
     * 用户的电子邮件地址。
     * 用途：
     * - 重要通知发送
     * - 密码重置等安全功能
     * - 用户信息搜索
     * - 系统消息推送
     * 
     * 验证：应该符合邮箱地址格式规范。
     */
    private String email;
}
