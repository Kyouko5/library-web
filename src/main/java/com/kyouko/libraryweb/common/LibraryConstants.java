package com.kyouko.libraryweb.common;

/**
 * 图书馆系统常量定义类
 * 
 * 集中定义系统中使用的所有常量值，便于维护和管理。
 * 避免在代码中使用魔法数字和硬编码字符串。
 * 
 * 常量分类：
 * - 用户角色常量：定义不同用户角色的标识值
 * - 借阅状态常量：定义图书借阅的各种状态
 * - 业务规则常量：定义系统的业务规则参数
 * 
 * 使用说明：
 * 1. 所有常量都使用public static final修饰
 * 2. 常量名使用大写字母和下划线命名
 * 3. 常量值的修改需要考虑对整个系统的影响
 * 
 * @author kyouko
 * @version 1.0
 */
public class LibraryConstants {

    // ==================== 用户角色相关常量 ====================
    
    /**
     * 管理员角色标识
     * 
     * 管理员拥有系统的最高权限，可以：
     * - 管理图书信息（增删改查）
     * - 管理用户账户
     * - 查看所有借阅记录
     * - 处理图书借还操作
     * - 查看系统统计数据
     * 
     * 在用户表的role字段中，值为"1"表示管理员
     */
    public static final String ROLE_ADMIN_NUM = "1";

    /**
     * 读者角色标识
     * 
     * 读者是普通用户，权限有限，只能：
     * - 查看图书信息
     * - 借阅和归还图书
     * - 查看自己的借阅记录
     * - 修改个人信息
     * - 续借图书（在规定次数内）
     * 
     * 在用户表的role字段中，值为"2"表示读者
     */
    public static final String ROLE_READER_NUM = "2";

    // ==================== 借阅状态相关常量 ====================
    
    /**
     * 图书借出状态
     * 
     * 表示图书已被用户借出，尚未归还。
     * 对应借阅记录表中status字段的值。
     * 
     * 当图书处于此状态时：
     * - 图书库存会相应减少
     * - 用户可以申请续借（如果未超过续借次数限制）
     * - 系统会跟踪借阅时间，超期会有相应提醒
     */
    public static final String STATUS_LEND = "1";

    /**
     * 图书归还状态
     * 
     * 表示图书已被用户归还，借阅记录结束。
     * 对应借阅记录表中status字段的值。
     * 
     * 当图书处于此状态时：
     * - 图书库存会相应增加
     * - 借阅记录标记为完成
     * - 用户可以继续借阅其他图书
     */
    public static final String STATUS_RETURN = "2";

    // ==================== 业务规则相关常量 ====================
    
    /**
     * 最大续借次数限制
     * 
     * 定义每本图书的最大续借次数。
     * 用于防止用户无限续借，确保图书的流通性。
     * 
     * 业务规则说明：
     * - 每本图书最多只能续借1次
     * - 续借会延长归还期限（通常延长与初始借阅期相同的时间）
     * - 超过此次数限制后，用户必须先归还图书才能重新借阅
     * 
     * 注意：修改此值会影响所有新的借阅记录，
     * 但不会影响已存在的借阅记录的续借次数统计
     */
    public static final int MAX_RENEW_TIMES = 1;

}